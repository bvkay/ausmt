"""C34/D1+D3 — the gw-runner generates LICENSE.md + README.md into a submitted package at intake
(after safe-extract, before validation), fail-closed and non-destructive.

Each test states its failure criterion and tests an independent observable (real bytes on disk, the
REAL validator's report), never metadata self-consistency.

The engine's stdlib-only _license_text leaf (D2) is imported here the way the runner reaches it in
the stack-less lane: engine/extract on sys.path (the leaf pulls no heavy stack, so this needs no
mt_metadata). The runner's own _generate_intake_files uses the SAME import path.
"""
from __future__ import annotations

import subprocess
import sys
from datetime import datetime, timezone
from pathlib import Path

# Make the engine's stdlib-only _license_text leaf importable (the runner's intake module reaches it
# via `from extract._license_text import ...`, falling back to the bare sibling name — this insert
# feeds that fallback in the local/gateway test lane where `extract` is not pip-installed).
_ENGINE_DIR = Path(__file__).resolve().parents[2] / "engine"
sys.path.insert(0, str(_ENGINE_DIR / "extract"))

from gateway.runner import intake  # noqa: E402
from gateway.runner import runner  # noqa: E402
from gateway.tests.conftest import require_validator_dir  # noqa: E402

_NOW = datetime(2026, 7, 7, 13, 30, tzinfo=timezone.utc)
_STAMP_PREFIX = "Generated by the AusMT gateway from the survey's declared metadata on"

# A structured (Prototype-20) survey with a recognised, redistributable licence + two EDIs. Enough
# for the real validator to reach a no-FAIL verdict once LICENSE.md/README.md are present.
_SURVEY_YAML = """\
schema_version: "0.2"
slug: intake-survey-2026
project_name: Intake Test Survey
version: 1.0.0
country: Australia
organisation:
  name: University of Example
abstract: >
  A two-station broadband deployment used to exercise intake file generation.
dates: { start: 2019, end: 2021 }
license: CC-BY-4.0
access:
  level: open
  embargo_until: null
"""


def _make_package(root: Path, *, survey_yaml: str = _SURVEY_YAML, n_edi: int = 2) -> Path:
    """Build an extracted-package-shaped folder <root>/<slug>/ with survey.yaml + n_edi EDIs, the
    layout safe_extract produces (package_dir/<slug>/...)."""
    pkg = root / "intake-survey-2026"
    (pkg / "transfer_functions" / "edi").mkdir(parents=True)
    (pkg / "survey.yaml").write_text(survey_yaml, encoding="utf-8")
    for i in range(n_edi):
        # A minimal but validator-clean EDI: coordinates in HEAD + a FREQ block (the real
        # validate_survey.py FAILs edi_parse otherwise), so the only structure signals in play are the
        # LICENSE.md/README.md presence WARNINGs this feature targets.
        (pkg / "transfer_functions" / "edi" / f"S{i:02d}.edi").write_text(
            ">HEAD\n  LAT=-30:08:45.2\n  LONG=136:58:12.0\n>END\n>FREQ ORDER=INC //1\n  1.0\n",
            encoding="utf-8")
    return pkg


# --------------------------------------------------------------------------------------------------
# D3 — generation into a package that lacks the files.
# --------------------------------------------------------------------------------------------------
def test_generates_both_files_with_stamp(tmp_path):
    # FAILS IF either file is missing after generation, or either lacks the machine stamp on its first
    # line. proven RED by disabling generation (return [] from generate_intake_files -> no files).
    pkg = _make_package(tmp_path)
    written = intake.generate_intake_files(pkg, now_utc=_NOW)
    assert written == ["LICENSE.md", "README.md"], written
    lic = (pkg / "LICENSE.md").read_text(encoding="utf-8")
    rdm = (pkg / "README.md").read_text(encoding="utf-8")
    assert lic.startswith(_STAMP_PREFIX), lic[:120]
    assert rdm.startswith(_STAMP_PREFIX), rdm[:120]
    assert "2026-07-07" in lic.splitlines()[0]
    assert "Replace via a metadata edit or resubmission." in lic.splitlines()[0]


def test_license_md_text_equals_engine_bundle_text(tmp_path):
    # Single-source proof (D2): the rights body inside LICENSE.md is the ENGINE's own
    # license_instrument_text output for the same id — so LICENSE.md and the bundle LICENSE.txt can
    # never carry divergent rights wording. FAILS IF intake stops delegating to _license_text.
    import _license_text as lt  # noqa: PLC0415
    pkg = _make_package(tmp_path)
    intake.generate_intake_files(pkg, now_utc=_NOW)
    lic = (pkg / "LICENSE.md").read_text(encoding="utf-8")
    # The engine's rights text for this survey (org name + citation year), the same call intake makes.
    expected_rights = lt.license_instrument_text("CC-BY-4.0", "University of Example", "2021")
    assert expected_rights in lic, lic
    assert "CC-BY-4.0" in lic and "creativecommons.org/licenses/by/4.0" in lic


def test_readme_carries_declared_metadata(tmp_path):
    # README skeleton (D3): name, organisation, dates, station count, licence, abstract, citation —
    # all from survey.yaml. FAILS IF a required field is dropped from the skeleton.
    pkg = _make_package(tmp_path, n_edi=2)
    intake.generate_intake_files(pkg, now_utc=_NOW)
    rdm = (pkg / "README.md").read_text(encoding="utf-8")
    assert "# Intake Test Survey" in rdm
    assert "University of Example" in rdm
    assert "2019–2021" in rdm
    assert "Stations: 2" in rdm
    assert "CC-BY-4.0" in rdm
    assert "broadband deployment" in rdm         # the abstract
    assert "## Citation" in rdm
    assert "(2021)" in rdm                        # citation year


