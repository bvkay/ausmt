def test_runner_generate_intake_files_never_raises(tmp_path, monkeypatch):
    # runner._generate_intake_files is best-effort: if intake blows up, it returns [] (never
    # propagates) so a valid package is not wrongly quarantined. FAILS IF the swallow is removed.
    def boom(*_a, **_k):
        raise RuntimeError("simulated intake failure")

    monkeypatch.setattr(intake, "generate_intake_files", boom)
    # A single-package layout so _single_package_root resolves.
    _make_package(tmp_path)
    assert runner._generate_intake_files(tmp_path) == []


# --------------------------------------------------------------------------------------------------
# D1 — end-to-end through the REAL validator: the two structure WARNINGs flip to PASS in one run.
# --------------------------------------------------------------------------------------------------
def _run_real_validator(validator_dir: Path, package_root: Path, out_json: Path) -> dict:
    import json
    validator_file = validator_dir / "validate_survey.py"
    subprocess.run([sys.executable, str(validator_file), str(package_root), "--json", str(out_json)],
                   capture_output=True, text=True)
    return json.loads(out_json.read_text(encoding="utf-8"))


def _structure_levels(report: dict, needle: str) -> list[str]:
    return [str(i.get("level")).upper() for i in report.get("items", [])
            if i.get("check") == "structure" and needle in str(i.get("message", ""))]


def test_real_validator_structure_warnings_flip_to_pass(tmp_path):
    # NO MOCKS: the REAL validate_survey.py (sibling or committed vendored). BEFORE generation the
    # validator WARNs 'LICENSE.md missing' + 'README.md missing'; AFTER generation both are PASS — in
    # the same package the runner would hand it. This is the D1 property: the generated files are
    # visible to the validator in the same run. FAILS IF generation stops running before validation,
    # or the validator no longer sees the generated files.
    validator_dir = require_validator_dir()
    pkg = _make_package(tmp_path)

    before = _run_real_validator(validator_dir, pkg, tmp_path / "before.json")
    lic_before = _structure_levels(before, "LICENSE.md")
    rdm_before = _structure_levels(before, "README.md")
    assert lic_before == ["WARNING"], before
    assert rdm_before == ["WARNING"], before

    written = intake.generate_intake_files(pkg, now_utc=_NOW)
    assert set(written) == {"LICENSE.md", "README.md"}, written

    after = _run_real_validator(validator_dir, pkg, tmp_path / "after.json")
    assert _structure_levels(after, "LICENSE.md") == ["PASS"], after
    assert _structure_levels(after, "README.md") == ["PASS"], after
    # And no NEW FAIL was introduced by the generated content.
    fails_after = [i for i in after.get("items", []) if str(i.get("level")).upper() in ("FAIL", "ERROR")]
    assert not fails_after, fails_after


def test_process_job_generates_files_before_validation(tmp_path, monkeypatch):
    # End-to-end through the runner's process_job with the validator + engine STUBBED: assert that by
    # the time the validator subprocess is invoked, the package on disk already carries the generated
    # LICENSE.md + README.md (generation ran BEFORE validation in _do_work). FAILS IF the generation
    # call is moved after the validator, or dropped.
    import io
    import json
    import zipfile
    from gateway import jobs

    # Build a real add-survey-shaped zip (single <slug>/ root) so safe_extract yields package/<slug>/.
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        zf.writestr("intake-survey-2026/survey.yaml", _SURVEY_YAML)
        zf.writestr("intake-survey-2026/transfer_functions/edi/S00.edi",
                    ">HEAD\n  LAT=-30:08:45\n  LONG=136:58:12\n>END\n")
    sid = "01INTAKE"
    cfg = runner.RunnerConfig(
        incoming_dir=tmp_path / "incoming", quarantine_dir=tmp_path / "quarantine",
        jobs_dir=tmp_path / "jobs", validator_path=str(tmp_path / "validator"), timeout_s=900)
    jobs.ensure_dirs(cfg.jobs_dir)
    quarantine = cfg.quarantine_dir / sid
    zpath = cfg.incoming_dir / f"{sid}.zip"
    zpath.parent.mkdir(parents=True, exist_ok=True)
    zpath.write_bytes(buf.getvalue())
    running = cfg.jobs_dir / "running" / f"{sid}.json"
    running.write_text(json.dumps({
        "submission_id": sid, "zip_path": str(zpath), "quarantine_dir": str(quarantine),
    }), encoding="utf-8")

    seen_at_validation = {}

    def fake_sub(cmd, *, cwd, deadline):
        if any("validate_survey.py" in c for c in cmd):
            # Record whether the generated files exist on disk AT VALIDATION TIME.
            folder = Path(cmd[2])
            seen_at_validation["license"] = (folder / "LICENSE.md").exists()
            seen_at_validation["readme"] = (folder / "README.md").exists()
            (Path(cmd[4])).write_text(json.dumps(
                {"items": [{"level": "PASS", "check": "structure", "message": "LICENSE.md present"}]}),
                encoding="utf-8")
            return subprocess.CompletedProcess(cmd, 0, stdout="[PASS   ] structure ok", stderr="")
        # engine preview stub: write a catalogue so the summary is populated.
        out_dir = Path(cmd[cmd.index("--out") + 1])
        out_dir.mkdir(parents=True, exist_ok=True)
        (out_dir / "catalogue.json").write_text("[[\"s0\"]]", encoding="utf-8")
        return subprocess.CompletedProcess(cmd, 0, stdout="", stderr="")

    monkeypatch.setattr(runner, "_run_subprocess", fake_sub)
    runner.process_job(cfg, running)

    assert seen_at_validation.get("license") is True, "LICENSE.md not present when the validator ran"
    assert seen_at_validation.get("readme") is True, "README.md not present when the validator ran"
    # And they physically persist in the (curator-reviewed) package.
    pkg_root = quarantine / "package" / "intake-survey-2026"
    assert (pkg_root / "LICENSE.md").exists() and (pkg_root / "README.md").exists()
    payload = json.loads((cfg.jobs_dir / "done" / f"{sid}.json").read_text(encoding="utf-8"))
    assert payload["outcome"] == jobs.OUTCOME_VALIDATED, payload
