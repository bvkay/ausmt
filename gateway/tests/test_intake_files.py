# D3 — fail-closed on an unrecognised licence: NO LICENSE.md (the WARNING stands), README still made.
# --------------------------------------------------------------------------------------------------
def test_unrecognised_license_generates_no_license_md(tmp_path):
    # FAILS IF a LICENSE.md is fabricated for a placeholder/typo licence. proven RED by weakening the
    # gate (recognised -> always True) which would write a LICENSE.md naming a bogus id.
    bad_yaml = _SURVEY_YAML.replace("license: CC-BY-4.0", "license: TBD by uploader")
    pkg = _make_package(tmp_path, survey_yaml=bad_yaml)
    written = intake.generate_intake_files(pkg, now_utc=_NOW)
    assert "LICENSE.md" not in written
    assert not (pkg / "LICENSE.md").exists(), "an unrecognised licence must NOT get a LICENSE.md"
    # README is still generated (it is not licence-gated) and states the raw declared licence.
    assert "README.md" in written
    assert (pkg / "README.md").exists()


def test_typo_license_generates_no_license_md(tmp_path):
    # The exact C6 typo hole, at intake: a single-char typo (letter O for zero) is NOT recognised.
    bad_yaml = _SURVEY_YAML.replace("license: CC-BY-4.0", "license: CC-BY-4.O")
    pkg = _make_package(tmp_path, survey_yaml=bad_yaml)
    written = intake.generate_intake_files(pkg, now_utc=_NOW)
    assert not (pkg / "LICENSE.md").exists(), written


def test_recognised_metadata_only_license_still_gets_license_md(tmp_path):
    # A recognised-but-metadata-only id ('ALL RIGHTS RESERVED') is NOT redistributable but IS
    # recognised, so it still gets a correct rights statement. FAILS IF the gate reused
    # `redistributable` (too narrow) instead of `recognised`.
    yaml_arr = _SURVEY_YAML.replace("license: CC-BY-4.0", "license: ALL RIGHTS RESERVED")
    pkg = _make_package(tmp_path, survey_yaml=yaml_arr)
    written = intake.generate_intake_files(pkg, now_utc=_NOW)
    assert "LICENSE.md" in written
    assert "ALL RIGHTS RESERVED" in (pkg / "LICENSE.md").read_text(encoding="utf-8")


# --------------------------------------------------------------------------------------------------
# D3 — never overwrite an existing file (byte-identity).
# --------------------------------------------------------------------------------------------------
def test_existing_files_are_never_overwritten(tmp_path):
    # FAILS IF generation clobbers a package's own LICENSE.md/README.md. proven RED by removing the
    # `if not path.exists()` guard, which would replace the submitter's bytes with the skeleton.
    pkg = _make_package(tmp_path)
    orig_lic = b"# The submitter's own hand-written LICENSE\nCustom terms.\n"
    orig_rdm = b"# The submitter's own README\nHand-written notes.\n"
    (pkg / "LICENSE.md").write_bytes(orig_lic)
    (pkg / "README.md").write_bytes(orig_rdm)
    written = intake.generate_intake_files(pkg, now_utc=_NOW)
    assert written == [], "nothing should be written when both files already exist"
    assert (pkg / "LICENSE.md").read_bytes() == orig_lic
    assert (pkg / "README.md").read_bytes() == orig_rdm


def test_one_existing_one_missing_only_generates_the_missing(tmp_path):
    # Mixed: an existing README is preserved byte-for-byte while a missing LICENSE.md is generated.
    pkg = _make_package(tmp_path)
    orig_rdm = b"# Submitter README, keep me\n"
    (pkg / "README.md").write_bytes(orig_rdm)
    written = intake.generate_intake_files(pkg, now_utc=_NOW)
    assert written == ["LICENSE.md"]
    assert (pkg / "README.md").read_bytes() == orig_rdm
    assert (pkg / "LICENSE.md").exists()


# --------------------------------------------------------------------------------------------------
# D3 — PII: generated content contains no submitter contact details (independent-observable grep).
# --------------------------------------------------------------------------------------------------
def test_generated_files_contain_no_pii(tmp_path):
    # The runner never sees the DB's submitter fields (a job carries only ids + paths), and intake
    # reads ONLY survey.yaml. Even so, pin it: put canary PII in the survey.yaml's OWN metadata-
    # adjacent-but-non-generated fields and assert none of it reaches the generated bytes. FAILS IF a
    # future skeleton starts echoing a contact/email/orcid field.
    pii_email = "submitter-canary@example.test"
    pii_orcid = "0000-0002-1825-0097"
    yaml_pii = _SURVEY_YAML + (
        f"submitter_email: {pii_email}\n"        # not a real schema field, but a hostile/legacy file could carry it
        f"lead_investigator:\n  name: Private Person\n  orcid: {pii_orcid}\n"
    )
    pkg = _make_package(tmp_path, survey_yaml=yaml_pii)
    intake.generate_intake_files(pkg, now_utc=_NOW)
    for fname in ("LICENSE.md", "README.md"):
        body = (pkg / fname).read_text(encoding="utf-8")
        assert pii_email not in body, f"{fname} leaked a submitter email"
        assert pii_orcid not in body, f"{fname} leaked an ORCID"
        assert "Private Person" not in body, f"{fname} leaked an investigator name"


